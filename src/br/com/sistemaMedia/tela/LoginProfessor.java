    private void btEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEntrarActionPerformed
        String usuario = varUsuario.getText().trim();
        String senha = String.valueOf(varSenha.getPassword()).trim();
        try {
            Professor professor = professorDao.pesquisarUserSenha(usuario, senha);
            if (chegarLogin(usuario, senha, professor)) {
                this.dispose();
                new Pricipal(professor).setVisible(true);
            }
        } catch (SQLException ex) {
            Logger.getLogger(LoginProfessor.class.getName()).log(Level.SEVERE,
                    null, ex);
        }


    }//GEN-LAST:event_btEntrarActionPerformed

