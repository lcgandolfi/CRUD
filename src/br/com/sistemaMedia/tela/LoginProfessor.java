    protected boolean chegarLogin(String usuario, String senha,
            Professor professor) {
        if (!RegraNegocio.validarStr(usuario)) {
            JOptionPane.showMessageDialog(null, "usuário incorreto");
            return false;

        } else if (!RegraNegocio.validarStr(senha)) {
            JOptionPane.showMessageDialog(null, "senha incorreto");
            return false;
        } else if (professor == null) {
            JOptionPane.showMessageDialog(null, "login nao encontrado");
            return false;
        }

        return true;

    }

