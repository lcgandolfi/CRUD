    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        String nome = varNome.getText().trim();
        int qtdAluno = Integer.parseInt(varQtdAluno.getText().trim());
        String diciplina = varDiciplina.getText().trim();
        String usuario = varUsuario.getText().trim();
        String senha = String.valueOf(varSenha.getPassword()).trim();
        double mediaPassar = Double.parseDouble(varMediaGeral.getText().trim());

        if (chegarCadastro(nome, qtdAluno, diciplina, usuario, senha,
                mediaPassar)) {
            professor = new Professor(nome, qtdAluno, diciplina, senha,
                    usuario, mediaPassar);
            try {
                professorDao.salvar(professor);
                new LoginProfessor().setVisible(true);
                JOptionPane.showMessageDialog(null, "professor cadastrado");
                this.dispose();

            } catch (SQLException ex) {
                Logger.getLogger(CadastroProfessor.class.getName()).
                        log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_btSalvarActionPerformed

