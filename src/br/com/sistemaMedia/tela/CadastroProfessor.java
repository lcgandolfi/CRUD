    protected boolean chegarCadastro(String nome, int qtdAluno, String diciplina,
            String usuario, String senha, double mediaPassar) {

        if (!RegraNegocio.validarStr(nome)) {
            JOptionPane.showMessageDialog(null, "nome incorreto");
            return false;

        } else if (!RegraNegocio.verificarNueroPossitivo(qtdAluno)) {
            JOptionPane.showMessageDialog(null, "quantidade de aluno incorreto");
            return false;

        } else if (!RegraNegocio.validarStr(diciplina)) {
            JOptionPane.showMessageDialog(null, "diciplina incorreto");
            return false;

        } else if (!RegraNegocio.validarStr(usuario)) {
            JOptionPane.showMessageDialog(null, "usuário incorreto");
            return false;

        } else if (!RegraNegocio.validarStr(senha)) {
            JOptionPane.showMessageDialog(null, "senha incorreto");
            return false;

        } else if (!RegraNegocio.verificarNueroPossitivo(mediaPassar)) {
            JOptionPane.showMessageDialog(null, "média geral incorreto");
            return false;

        }

        return true;

    }

